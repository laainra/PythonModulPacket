def volumeBalok():
    p = float(input("Masukkan ukuran panjang dalam centimeter:"))
    l = float(input("Masukkan ukuran lebar dalam centimeter:"))
    t = float(input("Masukkan ukuran tinggi dalam centimeter:"))
    volume = p * l * t
    print(f"Volume bangun ruang tersebut adalah {volume} cm\u00B3")

